# Numerical Operators

a = 10
b = 3

print("Addition:", a + b)
print("Substraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)

print("Modulus:", a % b)
print("Quotient:", a // b)
# Double division (//) returns the integer part of the division
print("Exponent:", a ** b)

# Use of assignment operators
a += 2
print(a)

# PEMDAS (Order of operations in mathematics)
# Parentheses, Exponents, Multiplication/Division, Addition/Subtraction

# Comparison operators
print(a > b)
print(a < b)

print(a >= b)
print(a <= b)

print(a == b)
print(a != b)
